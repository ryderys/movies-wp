	public function save_menus()
	{
		$menu_item = get_posts([
			'post_type' => 'nav_menu_item',
			'post_status' => 'publish',
			'numberposts' => -1,
		]);
		foreach ($menu_item as $key => $value) {
			wp_update_post(
				array(
					'ID' => $value->ID,
					'post_content' => $value->post_content,
				)
			);
		}
	}
