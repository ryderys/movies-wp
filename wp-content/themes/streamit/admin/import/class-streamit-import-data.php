	public function st_after_import_setup()
	{
		global $wp_filesystem;

		$content = '';

		// Assign menus to their locations.
		$menus     = wp_get_nav_menus();
		$locations = get_theme_mod('nav_menu_locations');

		if ($menus) {
			foreach ($menus as $menu) {
				if ($menu->name == 'Main Menu') {
					$locations['primary'] = $menu->term_id;
				}
			}
		}
		set_theme_mod('nav_menu_locations', $locations); // set menus to locations 

		//Save menus
		$this->save_menus();

		//reading settings default static pages
		$front_page_id = get_page_by_path('OTT Home');
		$blog_page_id  = get_page_by_path('Blog');

		update_option('show_on_front', 'page');
		update_option('page_on_front', $front_page_id->ID);
		update_option('page_for_posts', $blog_page_id->ID);

		//updates quick view icon
		update_option('woosq_button_type', 'link');
		update_option('woosq_button_position', '0');


		//replace custome link
		$this->replace_custom_link();

		$this->replace_footer_custom_link();

		//change woocommerce default shop page
		$id = get_page_by_path('shop-2');
		if ($id) {
			$id = $id->ID;
			update_option('woocommerce_shop_page_id', $id);
		}

		$checkout_page = get_page_by_path('checkout-2');
		if ($checkout_page) {
			$checkout_page_id = $checkout_page->ID;
			// Update WooCommerce setting for the Checkout page
			update_option('woocommerce_checkout_page_id', $checkout_page_id);
		}

		//change wishlist page
		$wishlist_page = get_page_by_path('wishlist-2');
		if ($wishlist_page) {
			$wishlist_page_id = $wishlist_page->ID;
			// Update YITH Wishlist page option
			update_option('yith_wcwl_wishlist_page_id', $wishlist_page_id);
			// Ensure the YITH plugin settings are refreshed
			do_action('yith_wcwl_after_wishlist_page_set', $wishlist_page_id);
			update_option('yith_wcwl_add_to_wishlist_text', '');
		}

		// Import Elementor settings
		$import_file = trailingslashit(get_template_directory()) . 'admin/import/Demo/elementor-site-settings.json';

		if (file_exists($import_file)) {
			global $wp_filesystem;

			// Initialize the WordPress filesystem
			if (empty($wp_filesystem)) {
				require_once ABSPATH . '/wp-admin/includes/file.php';
				WP_Filesystem();
			}

			// Read the contents of the file
			$content = $wp_filesystem->get_contents($import_file);

			if (!empty($content)) {
				// Decode the JSON content
				$site_settings = json_decode($content, true);

				if (is_array($site_settings)) {
					// Get the active Elementor kit ID
					$default_post_id = get_option('elementor_active_kit');

					if (!empty($default_post_id)) {
						// Update Elementor site settings
						update_post_meta($default_post_id, '_elementor_page_settings', $site_settings);
						error_log('Elementor site settings have been successfully imported.');
					} else {
						error_log('No active Elementor kit found.');
					}
				} else {
					error_log('Failed to decode the site settings JSON.');
				}
			} else {
				error_log('The site settings file is empty.');
			}
		} else {
			error_log('The site settings file does not exist.');
		}

		// Retrieve existing settings or initialize as an empty array if not found
		$this->quick_view_options();



		//Elementor post-types settings for edit with elementor option
		$enable_edit_with_elementor = [
			"post",
			"page",
		];
		update_option('elementor_cpt_support', $enable_edit_with_elementor);

		// save woof setting
		$woof_setting_file =  trailingslashit(get_template_directory()) . 'admin/import/Demo/streamit-woof-setting.json';
		if (file_exists($woof_setting_file)) {
			$content =  $wp_filesystem->get_contents($woof_setting_file);
			if (!empty($content)) {
				$woof_options = json_decode($content, true);
				foreach ($woof_options as $option_name => $option_data) {
					if (is_serialized($option_data)) {
						$option_data = unserialize($option_data);
					}
					update_option($option_name, $option_data);
				}
			}
		}

		// Update the permalink structure
		update_option('permalink_structure', '/%postname%/');
		flush_rewrite_rules();

		//REGISTERS 3 DEFAULT PLAN FOR PMP
		$this->register_default_pmp_plans();

		//GENERATE PMPRO PAGES
		$this->generate_default_pmpro_pages();
		

		//To clear Elementor cache after import
		if (
			class_exists('\Elementor\Plugin') &&
			isset(\Elementor\Plugin::$instance) &&
			isset(\Elementor\Plugin::$instance->files_manager)
		) {
			\Elementor\Plugin::$instance->files_manager->clear_cache();
		}

	}

	public function quick_view_options()
	{
		$options = get_option('woosq_settings', array());

		// Ensure the settings array is properly initialized
		if (is_array($options)) {
			// Update the specific quick view options
			$options['button_type'] = 'link'; // Set button type to 'link'
			$options['button_icon'] = 'only'; // Set button icon to 'only'

			// Save the updated options back to the database
			update_option('woosq_settings', $options);
		}
	}

	public function replace_custom_link()
	{
		//change menu custom link
		$old_domain = 'https://streamit-wordpress.iqonic.design';
		$new_domain = home_url();

		// Define the menu slug.
		$menu_name = 'main-menu'; // Replace with your menu slug.

		// Get the menu object by slug.
		$menu = wp_get_nav_menu_object($menu_name);

		// Check if the menu exists.
		if (!$menu) {
			error_log('Menu not found with slug: ' . $menu_name);
			return;
		}

		// Get the menu ID and items.
		$menu_id = $menu->term_id;
		$menu_items = wp_get_nav_menu_items($menu_id);

		// Check if there are menu items.
		if (!empty($menu_items)) {
			foreach ($menu_items as $menu_item) {
				// Check if it's a custom link and starts with the old domain.
				if ($menu_item->type === 'custom' && strpos($menu_item->url, $old_domain) === 0) {
					// Extract the path after the old domain.
					$path = str_replace($old_domain, '', $menu_item->url);

					// Create the new URL by appending the path to the new domain.
					$new_url = trailingslashit($new_domain) . ltrim($path, '/');

					// Update the menu item's URL only.
					wp_update_nav_menu_item($menu_id, $menu_item->ID, [
						'menu-item-object-id' => $menu_item->object_id,
						'menu-item-parent-id' => $menu_item->menu_item_parent,
						'menu-item-db-id'     => $menu_item->ID,
						'menu-item-object'    => $menu_item->object,
						'menu-item-type'      => $menu_item->type,
						'menu-item-url'       => esc_url($new_url),
						'menu-item-title'     => $menu_item->title,
						'menu-item-status'    => $menu_item->post_status,
					]);
				}
			}
		} else {
			error_log('No menu items found for menu ID: ' . $menu_id);
		}
	}

