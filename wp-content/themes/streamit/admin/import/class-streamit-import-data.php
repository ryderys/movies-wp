	/**
	 * Adds the action and filter hooks to integrate with WordPress.
	 */

	public function __construct()
	{

		add_filter('merlin_generate_child_functions_php', 	array($this, 'st_generate_child_functions_php'), 10, 2);
		add_filter('merlin_generate_child_style_css', 		array($this, 'st_generate_child_style_css'), 10, 5);
		add_filter('merlin_import_files', 					array($this, 'st_import_files'));
		add_action('merlin_after_all_import', 				array($this, 'st_after_import_setup'));
		add_filter('merlin_generate_child_screenshot', 		array($this, 'st_generate_child_screenshot'));
	}

	public function st_generate_child_functions_php($output, $slug)
	{

		$slug_no_hyphens = strtolower(preg_replace('#[^a-zA-Z]#', '', $slug));

		$output = "
		<?php
		/**
		 * Theme functions and definitions.
		 */
		add_action( 'wp_enqueue_scripts', '{$slug_no_hyphens}_enqueue_styles' ,99);

		function {$slug_no_hyphens}_enqueue_styles() {
				
			wp_enqueue_style( 'parent-style', get_stylesheet_directory_uri() . '/style.css'); 
			wp_enqueue_style( 'child-style',get_stylesheet_directory_uri() . '/style.css');
		}

		/**
		 * Set up My Child Theme's textdomain.
		*
		* Declare textdomain for this child theme.
		* Translations can be added to the /languages/ directory.
		*/
		function {$slug_no_hyphens}_child_theme_setup() {
			load_child_theme_textdomain( 'streamit', get_stylesheet_directory() . '/languages' );
		}
		add_action( 'after_setup_theme', '{$slug_no_hyphens}_child_theme_setup' );
	";

		// Let's remove the tabs so that it displays nicely.
		$output = trim(preg_replace('/\t+/', '', $output));

		// Filterable return.
		return $output;
	}

